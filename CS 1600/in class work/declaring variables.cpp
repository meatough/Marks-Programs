//declaring variables

#include<iostream>
using namespace std;

int main()
{ 	
	int a;				//declaring variable
	a = 27;				//assigning statement
	int b = 17;			//initializing variables
		
	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	
	cout << "a + b = "	<< a + b << endl;
	cout << "a - b = "	<< a - b << endl;
	cout << "a * b = "	<< a * b << endl;
	cout << "a / b = "	<< a / b << endl;
	cout << "a % b = "	<< a % b << endl;	
}